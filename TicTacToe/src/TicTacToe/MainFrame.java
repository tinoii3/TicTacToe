    private static final int[][] WINNING_COMBINATIONS = {
        {0, 1, 2}, {3, 4, 5}, {6, 7, 8}, // แนวนอน
        {0, 3, 6}, {1, 4, 7}, {2, 5, 8}, // แนวตั้ง
        {0, 4, 8}, {2, 4, 6} // แนวทแยง
    };

    private boolean checkWin(int[] playerWinArray) {
        for (int[] combination : WINNING_COMBINATIONS) {
            if (playerWinArray[combination[0]] == 1
                    && playerWinArray[combination[1]] == 1
                    && playerWinArray[combination[2]] == 1) {
                return true;
            }
        }
        return false;
    }

    private void checkGameStatus() {
        if (checkWin(pXwin)) {
            JOptionPane.showMessageDialog(this, "Player X Wins!");
            xCount++;
            lblwinX.setText(String.valueOf(xCount));
            resetButtons();
        } else if (checkWin(pOwin)) {
            JOptionPane.showMessageDialog(this, "Player O Wins!");
            oCount++;
            lblwinO.setText(String.valueOf(oCount));
            resetButtons();
        } else if (tie == 9) {
            JOptionPane.showMessageDialog(this, "It's a Tie!");
            resetButtons();
        }
    }

    private void resetGame() {
        turn = 2;
        tie = 0;
        Arrays.fill(buttonused, 0);
        Arrays.fill(pXwin, 0);
        Arrays.fill(pOwin, 0);
        lblwinX.setText(String.valueOf(0));
        lblwinO.setText(String.valueOf(0));
    }

