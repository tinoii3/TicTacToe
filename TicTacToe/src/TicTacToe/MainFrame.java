    private void updateButton(int index, JButton button) {
        if (buttonused[index] == 0) {
            buttonused[index] = 1;
            tie++;

            if (turn % 2 == 0) { // Player X
                button.setText("X");
                button.setForeground(Color.ORANGE);
                pXwin[index] = 1;
            } else { // Player O
                button.setText("O");
                button.setForeground(Color.WHITE);
                pOwin[index] = 1;
            }
            turn++;
            checkGameStatus();
        } else {
            JOptionPane.showMessageDialog(this, "This Button is Already Used!");
        }
    }

    private void resetButtons() {
        JButton[] buttons = {btn1, btn2, btn3, btn4, btn5, btn6, btn7, btn8, btn9};
        for (JButton button : buttons) {
            button.setText("");
            button.setForeground(Color.BLACK);
        }
        Arrays.fill(buttonused, 0);
        Arrays.fill(pXwin, 0);
        Arrays.fill(pOwin, 0);
        tie = 0;
        turn = 2;
    }


